package org.jey;

public class Aula {
    public String dia;
    public String hora;
    public String professor;
    public String idioma;
    public String nivel;
    public String turma;

    public Aula(String dia, String hora, String professor, String idioma, String nivel, String turma) {
        this.dia = dia;
        this.hora = hora;
        this.professor = professor;
        this.idioma = idioma;
        this.nivel = nivel;
        this.turma = turma;
    }
}
